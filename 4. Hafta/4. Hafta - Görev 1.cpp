#include <iostream>
using namespace std;
int main()
{
    int a = 14, b = 3;
    cout << "Ekleme: " << (a + b) << endl;
    cout << "Cikarma: " << (a - b) << endl;
    cout << "Carpma: " << (a * b) << endl;
    cout << "Bolme: " << (a / b) << endl;
    cout << "Mod alma: " << (a % b) << endl;
    cout << "Arttirma: " << a++ << endl;
    cout << "Arttirma: " << ++a << endl;
    cout << "Azaltma: " << b-- << endl;
    cout << "Azaltma: " << --b << endl;

return 0;
}

